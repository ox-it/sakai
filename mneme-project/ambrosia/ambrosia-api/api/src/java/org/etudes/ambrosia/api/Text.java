package org.etudes.ambrosia.api;

/**
 * UiText presents textual information to the user.<br />
 * The text can be either a property reference or a message.
 */
public interface Text extends Component
{
	/**
	 * Set the text message.
	 * 
	 * @param selector
	 *        The message selector.
	 * @param references
	 *        one or more (or an array) of UiPropertyReferences to form the additional values in the formatted message.
	 */
	Text setText(String selector, PropertyReference... references);

	/**
	 * Set the title text.
	 * 
	 * @param selector
	 *        The message selector.
	 * @param references
	 *        one or more (or an array) of UiPropertyReferences to form the additional values in the formatted message.
	 */
	Text setTitle(String selector, PropertyReference... references);

	/**
	 * Set the treatment.
	 * 
	 * @param treatment
	 *        The section treatment.
	 * @return self.
	 */
	Text setTreatment(String treatment);
}
